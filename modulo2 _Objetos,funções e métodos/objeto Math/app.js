// o objeto Math


const area = 7.7
console.log(Math.round(area)) // segue a regra 
console.log(Math.floor(area)) // sempre pra baixo
console.log(Math.ceil(area)) // sempre pra cima
console.log(Math.trunc(area)) // remove o decimal 


